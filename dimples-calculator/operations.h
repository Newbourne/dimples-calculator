class Operations {
public:
    static double add(double d1, double d2){
        return d1 + d2;
    }

    static double subtract(double d1, double d2){
        return d1 - d2;
    }

    static double divide(double d1, double d2){
        return d1 / d2;
    }

    static double multiple(double d1, double d2){
        return d1 * d2;
    }
};
