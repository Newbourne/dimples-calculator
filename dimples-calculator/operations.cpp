#include "Operations.h"

double Operations::add(double d1, double d2){
    return d1 + d2;
}

double Operations::subtract(double d1, double d2){
    return d1 - d2;
}

double Operations::divide(double d1, double d2){
    return d1 / d2;
}

 double Operations::multiple(double d1, double d2){
    return d1 * d2;
}
